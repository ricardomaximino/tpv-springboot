    private void porFecha() {
        //
        LocalDate fecha;
        try {
            fecha = LocalDate.parse(txtFecha.getText(), DateTimeFormatter.ISO_DATE);
        } catch (Exception ex) {
            fecha = LocalDate.now();
        }

        FacturaTableModel model = (FacturaTableModel) tabla.getModel();
        model.getListaDeFactura().clear();
        if (txtFecha.getText().isEmpty()) {
            model.getListaDeFactura().addAll(lista);
        } else {
            for (Factura f : lista) {
                if (f.getFecha().isEqual(fecha)) {
                    model.getListaDeFactura().add(f);
                }
            }
        }
        model.fireTableDataChanged();

    }

    private void clear() {
        txtFecha.setText("");
    }

